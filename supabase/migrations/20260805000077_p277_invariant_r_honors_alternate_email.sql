DECLARE v_r integer;
BEGIN
  SELECT violation_count INTO v_r FROM public.check_schema_invariants()
  WHERE invariant_name = 'R_approved_application_has_member';
  IF v_r IS DISTINCT FROM 0 THEN
    RAISE EXCEPTION 'p277 invariant-R fix: R still reports % violation(s) post-apply', v_r;
  END IF;
END
$sanity$;
