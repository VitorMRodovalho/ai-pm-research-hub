#!/usr/bin/env node
/**
 * Generate the Phase C body-hash drift allowlist baseline (p175).
 *
 * Fetches live function bodies via `_audit_list_public_function_bodies()`,
 * diffs against the latest CREATE FUNCTION captures in
 * supabase/migrations/, and writes every currently-drifted key
 * (`name@normalized_args`) to docs/audit/RPC_BODY_DRIFT_ALLOWLIST_P175.txt.
 *
 * Run after capture migrations (drift recovery) to ratchet the baseline DOWN.
 *
 * Usage:
 *   SUPABASE_URL=https://…supabase.co SUPABASE_SERVICE_ROLE_KEY=eyJ… \
 *   node scripts/generate-rpc-drift-baseline.mjs
 */
import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { loadLatestCaptures, diffLiveVsCaptures } from '../tests/helpers/rpc-body-drift-parser.mjs';

const SUPABASE_URL = process.env.SUPABASE_URL || process.env.PUBLIC_SUPABASE_URL;
const SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
if (!SUPABASE_URL || !SERVICE_ROLE_KEY) {
  console.error('Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY env vars');
  process.exit(1);
}

const ROOT = process.cwd();
const MIGRATIONS_DIR = resolve(ROOT, 'supabase/migrations');
const ALLOWLIST_OUT = resolve(ROOT, 'docs/audit/RPC_BODY_DRIFT_ALLOWLIST_P175.txt');

async function callRpc() {
  const url = `${SUPABASE_URL}/rest/v1/rpc/_audit_list_public_function_bodies`;
  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      apikey: SERVICE_ROLE_KEY,
      Authorization: `Bearer ${SERVICE_ROLE_KEY}`,
    },
    body: JSON.stringify({}),
  });
  if (!res.ok) {
    throw new Error(`RPC failed: ${res.status} ${await res.text()}`);
  }
  return res.json();
}

const liveRows = await callRpc();
console.log(`Live functions: ${liveRows.length}`);

const captures = loadLatestCaptures(MIGRATIONS_DIR);
console.log(`Migration files: ${captures.fileCount}`);
console.log(`Unique captured keys: ${captures.latest.size}`);

const diff = diffLiveVsCaptures(liveRows, captures);

console.log('\n========== DRIFT SUMMARY ==========');
console.log(`Clean:               ${diff.clean}`);
console.log(`Drifted DEFINITE:    ${diff.driftedDefinite.length}`);
console.log(`Drifted SUSPECT:     ${diff.driftedSuspect.length}`);
console.log(`Orphans TRUE:        ${diff.orphansTrue.length}`);
console.log(`Orphans OVERLOAD:    ${diff.orphansOverload.length}`);
console.log(`Extinct (in migrations, not in DB): ${diff.extinct.length}`);

const allKeys = [
  ...diff.driftedDefinite.map(r => r.key),
  ...diff.driftedSuspect.map(r => r.key),
].sort();

const header = `# Phase C body-hash drift allowlist (p175).
#
# Each entry is "name@normalized_args" for a function whose live body
# (md5 of whitespace-normalized prosrc) does NOT match the latest
# CREATE FUNCTION capture in supabase/migrations/.
#
# Baseline generated by scripts/generate-rpc-drift-baseline.mjs at p175
# bootstrap. Ratchet DOWN by capturing bodies via apply_migration (Phase
# B drift recovery) and re-running this script to regenerate.
#
# Test enforces:
#   1. No NEW drift outside this allowlist.
#   2. Allowlist stays in sync — entries that are now clean (or extinct)
#      must be removed AND the baseline constant in
#      tests/contracts/rpc-migration-coverage.test.mjs decremented.
#   3. Size matches the constant in the test file.
#
# See docs/audit/RPC_BODY_DRIFT_AUDIT_P50.md § Phase C charter.

`;

writeFileSync(ALLOWLIST_OUT, header + allKeys.join('\n') + '\n');
console.log(`\nWrote ${allKeys.length} drift keys to ${ALLOWLIST_OUT}`);
